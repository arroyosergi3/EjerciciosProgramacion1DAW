package capitulo05.bloque01Herencia.coleccionAntiguedades;

public class Antiguo {

	protected int añoF, precio;
	protected String origen;
	
	public Antiguo() {

		
	}
	
	
	
	//GETTERS Y SETTERS
	public int getAñoF() {
		return añoF;
	}
	public void setAñoF(int añoF) {
		this.añoF = añoF;
	}
	public int getPrecio() {
		return precio;
	}
	public void setPrecio(int precio) {
		this.precio = precio;
	}
	public String getOrigen() {
		return origen;
	}
	public void setOrigen(String origen) {
		this.origen = origen;
	}
	
	
	
	
	
	
	
	
	
	
}
