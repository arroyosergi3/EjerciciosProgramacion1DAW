package capitulo08.portatiles.entities;

public class Pais {
	private int id, idContienente;
	private String descripcion;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIdContienente() {
		return idContienente;
	}
	public void setIdContienente(int idContienente) {
		this.idContienente = idContienente;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	

}
