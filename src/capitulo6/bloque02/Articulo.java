package capitulo6.bloque02;

public class Articulo {

	private int codigo, nEs, stock;
	
	public Articulo() {
		
	}
	
	
	
	
	
	
	
	
	
	
	
	@Override
	public String toString() {
		return "Articulo [codigo=" + codigo + ", nEs=" + nEs + ", stock=" + stock + "]";
	}











	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getnEs() {
		return nEs;
	}
	public void setnEs(int nEs) {
		this.nEs = nEs;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	
	
	
	
	
	
	
	
}
